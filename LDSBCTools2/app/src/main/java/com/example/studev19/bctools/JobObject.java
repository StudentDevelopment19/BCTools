public class JobObject {
    private String position, company, location, description;

    public String getJobPosition() {
        return position;
    }

    public void setJobPosition(String mPosition) {
        this.position = mPosition;
    }

    public String getJobCompany() {
        return company;
    }

    public void setJobCompany(String mCompany) {
        this.company = mCompany;
    }

    public String getJobLocation() {
        return location;
    }

    public void setJobLocation(String mLocation) {
        this.location = mLocation;
    }

    public String getJobDescription() {
        return description;
    }

    public void setJobDescription(String mDescription) {
        this.description = mDescription;
    }

}
